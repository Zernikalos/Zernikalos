package zernikalos.utils

fun randomNumId(): Int {
    return crc32(randomId().encodeToByteArray())
}

fun randomId(): String {
    return getRandomString(16)
}

private fun getRandomString(length: Int) : String {
    val allowedChars = ('A'..'Z') + ('a'..'z') + ('0'..'9')
    return (1..length)
        .map { allowedChars.random() }
        .joinToString("")
}